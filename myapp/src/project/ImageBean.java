package project;

public class ImageBean 
{
	int id;
	String name;
	String attr;
	int count;
	
	public int getId() 
	{
		return id;
	}
	public void setId(int id) 
	{
		this.id = id;
	}
	public String getName() 
	{
		return name;
	}
	public void setName(String name) 
	{
		this.name = name;
	}
	public String getAttr() 
	{
		return attr;
	}
	public void setAttr(String attr) 
	{
		this.attr = attr;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	} 
	
}
